train_data = pd.read_csv("Training.csv")
test_data = pd.read_csv("Testing.csv")

# Convert relevant columns to numeric
for col in ["Close", "Open", "High", "Low"]:
    train_data[col] = pd.to_numeric(train_data[col], errors="coerce")
    test_data[col] = pd.to_numeric(test_data[col], errors="coerce")

# Drop NaN values in both datasets
train_data = train_data.dropna(subset=["Close", "Open", "High", "Low"])
test_data = test_data.dropna(subset=["Close", "Open", "High", "Low"])

# Prepare input (features) and output (target)
X_train_full = train_data[["Open", "High", "Low"]].values
y_train_full = train_data["Close"].values
X_test = test_data[["Open", "High", "Low"]].values
y_test = test_data["Close"].values

# Split the training data into 80% training and 20% validation
X_train, X_val, y_train, y_val = train_test_split(X_train_full, y_train_full, test_size=0.2, random_state=42)

# Reshape input data for Bi-LSTM (samples, timesteps, features)
X_train = X_train.reshape((X_train.shape[0], 1, X_train.shape[1]))
X_val = X_val.reshape((X_val.shape[0], 1, X_val.shape[1]))
X_test = X_test.reshape((X_test.shape[0], 1, X_test.shape[1]))

# Define the hybrid model
input_tensor = Input(shape=(X_train.shape[1], X_train.shape[2]))
# Bi-LSTM branch
bi_lstm = Bidirectional(LSTM(64, return_sequences=False, activation="relu"))(input_tensor)
# TimesNet branch
flattened_input = tf.keras.layers.Flatten()(input_tensor)
timesnet = timesnet_block(flattened_input, units=64)
# Concatenate both branches
merged = Concatenate()([bi_lstm, timesnet])
output = Dense(1)(merged)

model = Model(inputs=input_tensor, outputs=output)

# Compile the model
model.compile(optimizer="adam", loss="mse")

# Train the model
history = model.fit(X_train, y_train, validation_data=(X_val, y_val), epochs=40, batch_size=32, verbose=1)

# Make predictions
pred_val = model.predict(X_val)
pred_test = model.predict(X_test)

# Regression evaluation metrics for validation
mae_val = mean_absolute_error(y_val, pred_val)
mse_val = mean_squared_error(y_val, pred_val)
rmse_val = np.sqrt(mse_val)
mape_val = mean_absolute_percentage_error(y_val, pred_val)

