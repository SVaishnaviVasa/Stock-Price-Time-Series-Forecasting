import numpy as np
import pandas as pd
import tensorflow as tf
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import mean_absolute_error, mean_squared_error, mean_absolute_percentage_error
import matplotlib.pyplot as plt

# Load the training and testing datasets
train_data = pd.read_csv("Training.csv")
test_data = pd.read_csv("Testing.csv")

# Convert relevant columns to numeric
for col in ["Close", "Open", "High", "Low"]:
    train_data[col] = pd.to_numeric(train_data[col], errors="coerce")
    test_data[col] = pd.to_numeric(test_data[col], errors="coerce")

# Drop NaN values in both datasets
train_data = train_data.dropna(subset=["Close", "Open", "High", "Low"])
test_data = test_data.dropna(subset=["Close", "Open", "High", "Low"])

# Prepare input (features) and output (target)
X_train_full = train_data[["Open", "High", "Low"]].values
y_train_full = train_data["Close"].values

X_test = test_data[["Open", "High", "Low"]].values
y_test = test_data["Close"].values

# Flatten the data for scaling
X_train_full_flat = X_train_full.reshape(-1, X_train_full.shape[-1])  # 2D: (samples * timesteps, features)
X_test_flat = X_test.reshape(-1, X_test.shape[-1])  # 2D: (samples * timesteps, features)

# Standardize the data
scaler = StandardScaler()
X_train_full_scaled_flat = scaler.fit_transform(X_train_full_flat)
X_test_scaled_flat = scaler.transform(X_test_flat)

# Reshape back to 3D for Transformer
X_train_full_scaled = X_train_full_scaled_flat.reshape(X_train_full.shape)  # 3D: (samples, timesteps, features)
X_test_scaled = X_test_scaled_flat.reshape(X_test.shape)  # 3D: (samples, timesteps, features)

# Split the scaled training data into 80% training and 20% validation
X_train, X_val, y_train, y_val = train_test_split(
    X_train_full_scaled, y_train_full, test_size=0.2, random_state=42
)

# Reshape input data for Transformer (samples, timesteps, features)
X_train = X_train.reshape((X_train.shape[0], 1, X_train.shape[1]))
X_val = X_val.reshape((X_val.shape[0], 1, X_val.shape[1]))
X_test = X_test_scaled.reshape((X_test.shape[0], 1, X_test.shape[1]))

# Build the Transformer Model
def build_transformer_model(input_shape):
    inputs = tf.keras.Input(shape=input_shape)
    x = inputs

    # Multi-Head Self Attention
    attention_output = tf.keras.layers.MultiHeadAttention(num_heads=4, key_dim=64)(x, x)
    attention_output = tf.keras.layers.Dropout(0.01)(attention_output)
    attention_output = tf.keras.layers.LayerNormalization(epsilon=1e-6)(attention_output + x)

    # Feed Forward Network
    ffn = tf.keras.Sequential([
        tf.keras.layers.Dense(64, activation="relu"),
        tf.keras.layers.Dense(input_shape[-1]),
    ])
    ffn_output = ffn(attention_output)
    ffn_output = tf.keras.layers.Dropout(0.01)(ffn_output)
    ffn_output = tf.keras.layers.LayerNormalization(epsilon=1e-6)(ffn_output + attention_output)

    # Global Average Pooling and Output Layer
    pooled_output = tf.keras.layers.GlobalAveragePooling1D()(ffn_output)
    outputs = tf.keras.layers.Dense(1)(pooled_output)

    model = tf.keras.Model(inputs=inputs, outputs=outputs)
    return model

# Initialize and compile the model
transformer_model = build_transformer_model((X_train.shape[1], X_train.shape[2]))
transformer_model.compile(optimizer="adam", loss="mse")

# Early stopping to prevent overfitting
early_stopping = tf.keras.callbacks.EarlyStopping(
    monitor="val_loss", patience=5, restore_best_weights=True
)

# Train the model
history = transformer_model.fit(
    X_train, y_train, validation_data=(X_val, y_val), epochs=50, batch_size=32,
    verbose=1, callbacks=[early_stopping]
)

# Make predictions
pred_val = transformer_model.predict(X_val)
pred_test = transformer_model.predict(X_test)

